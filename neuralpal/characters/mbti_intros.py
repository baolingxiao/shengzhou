def build_intro_paragraphs(name: str, user_mbti: str) -> list[str]:
    """按用户 MBTI 生成首次自我介绍段落（已替换角色名，每段一条聊天气泡）。"""
    mbti = (user_mbti or "INFP").strip().upper()[:4]
    body = MBTI_INTRO_BODIES.get(mbti) or MBTI_INTRO_BODIES["INFP"]
    display_name = (name or "我的 AI 伴侣").strip() or "我的 AI 伴侣"
    text = body.replace(_NAME_PLACEHOLDER, display_name)
    parts: list[str] = []
    for block in re.split(r"\n\s*\n", text):
        block = block.strip()
        if not block:
            continue
        for line in block.split("\n"):
            line = line.strip()
            if line:
                parts.append(line)
    return parts
